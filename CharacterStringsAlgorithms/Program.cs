using System;

namespace CharacterStringsAlgorithms
{
    public class Program
    {
        public static void Main()
        {
            int i = 'C' - 'A';              

            Console.WriteLine(i);           // i = 2

            char j = (char)(3 + 'A');       

            Console.WriteLine(j);           // j = 'D'
        }
    }
}
